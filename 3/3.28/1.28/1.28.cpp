#include "stdafx.h"


int _tmain(int argc, _TCHAR* argv[])
{
	int flag = 0;

	switch(flag)
	{
	case 10:
		printf("10\n");
		break;

	case 20:
		printf("20\n");
		break;

	case 30:
		printf("30\n");
		break;

	case 40:
		printf("40\n");
		break;

	case 50:
		printf("50\n");
		break;

	default:
		printf("\n");
		break;
	}

	return 0;
}

